    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            //Criar variavel
            Connection conn;
            
            //Cria conexão com o banco de dados
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cadastro?useTimezone=true&serverTimezone=UTC", "root", "");
            
            String query = "SELECT * FROM cliente WHERE nome LIKE ?";
            
            PreparedStatement cmd;
            
            cmd = conn.prepareStatement(query);
            
            cmd.setString(1, "%" + jTextField1.getText() + "%");
            
            ResultSet rs;
            rs = cmd.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            //Para setar numero de linhas da tabela
            model.setNumRows(0);
            
            while (rs.next()){
                model.addRow(
                        new Object[]{
                            rs.getString("idcliente"), 
                            rs.getString("nome"), 
                            rs.getString("cpf"), 
                            rs.getString("endereco")});
      
            }
        } catch (ClassNotFoundException ex) {
            System.out.println("Não foi possivel encontrar a classe" + ex.getMessage());
        } catch (SQLException ex) {
            System.out.println("Ocorreu um erro ao tentar conectar a DB" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

