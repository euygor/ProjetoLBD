    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            //Criar variavel
            Connection con;
            
            String query;
            PreparedStatement stmt;
            //Cria conexão com o banco de dados
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cadastro?useTimezone=true&serverTimezone=UTC", "root", "");

            if(jTextField1.getText().equals("")){
            //Cria a string para inserir na db
            query = "INSERT INTO cliente (nome,cpf,endereco) VALUES (?,?,?)";
                
            //Cria comando
             stmt = con.prepareStatement(query);           
            
            //seta os valores na string de insert
            stmt.setString(1, jTextField4.getText());
            stmt.setString(2, jTextField2.getText());
            stmt.setString(3, jTextField3.getText());
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            } else {
            //Cria a string para atualizar dados na db
            query = "UPDATE cliente SET nome = ?, cpf = ?, endereco = ? WHERE idcliente = ?";
                
            //Cria comando
            stmt = con.prepareStatement(query);           
            
            //seta os valores na string de update
            stmt.setString(1, jTextField4.getText());
            stmt.setString(2, jTextField2.getText());
            stmt.setString(3, jTextField3.getText());
            stmt.setString(4, jTextField1.getText());
            JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
            }
          

            
            //executa o comando na db
            stmt.executeUpdate();
            //fecha os comandos e conexão
            stmt.close();
            con.close();
        } catch (ClassNotFoundException ex) {
            System.out.println("Não foi possivel encontrar a classe");
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("Ocorreu um erro ao tentar conectar a DB");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

